# Using c() to create a vector
numeric_vector <- c(1, 2, 3, 4, 5)

# Using the colon operator to create a sequence of numbers
sequence_vector <- 1:10

# Using seq() to create a sequence with a specific increment
sequence_vector_2 <- seq(from = 1, to = 10, by = 2)

# Using rep() to create a repeated sequence
repeated_vector <- rep(1:3, times = 3)

